fn main() {
   let num_one: u32 = 5;
   let num_two: u32 = 4;

   println!("{} + {} = {}.", num_one, num_two, num_one + num_two);
   println!("{} - {} = {}.", num_one, num_two, num_one - num_two);
   println!("{} * {} = {}.", num_one, num_two, num_one * num_two);
   println!("{} / {} = {}.", num_one, num_two, num_one / num_two);
   println!("{} % {} = {}.", num_one, num_two, num_one % num_two);
}
